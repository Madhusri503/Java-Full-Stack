public class Unary
{
public static void main(String[] args)
{
int a=6;

System.out.println("Unary plus:"+(+a));
System.out.println("Unary minus:"+(-a));
System.out.println("Increment (post):"+(a++));
System.out.println("Incremment (pre):"+(++a));
System.out.println("Decrement (post):"+(a--));
System.out.println("Decrement (pre):"+(--a));
}
}

